<#
Generate four deliberately-broken copies of scripts/run-co-evolution.ps1 for
Tier 4 seeded regression testing. Each copy plants a single known bug and is
named to describe what it breaks.

The eval MUST detect each regression (flag at least one dimension FAIL/PARTIAL
when that runner is used). If the eval scores all-PASS against a broken
runner, the eval is blind to that class of failure and the scorer must be
tightened.

Generated files:
  evals/tests/regressions/regression-a-skip-bounce.ps1
  evals/tests/regressions/regression-b-truncate-plan.ps1
  evals/tests/regressions/regression-c-no-verdict.ps1
  evals/tests/regressions/regression-d-fake-approve.ps1

Usage:
  powershell.exe -File evals/tests/Build-Regressions.ps1 [-Force]
#>
param(
    [switch]$Force
)

Set-StrictMode -Version Latest
$ErrorActionPreference = 'Stop'

$evalsRoot   = Split-Path -Parent $PSScriptRoot
$projectRoot = Split-Path -Parent $evalsRoot
$sourceRunner = Join-Path $projectRoot "scripts/run-co-evolution.ps1"
$outDir = Join-Path $PSScriptRoot "regressions"

if (-not (Test-Path -LiteralPath $sourceRunner)) {
    throw "source runner not found: $sourceRunner"
}
$null = New-Item -ItemType Directory -Path $outDir -Force

$source = Get-Content -Raw -LiteralPath $sourceRunner -Encoding UTF8

function Write-Regression {
    param([string]$Name, [string]$Content, [string]$Description)
    $path = Join-Path $outDir $Name
    if ((Test-Path -LiteralPath $path) -and -not $Force) {
        Write-Host "  skip (exists): $path"
        return
    }
    $banner = "# SEEDED REGRESSION: $Description`r`n# Auto-generated by Build-Regressions.ps1. Do not hand-edit.`r`n`r`n"
    Set-Content -LiteralPath $path -Value ($banner + $Content) -Encoding UTF8
    Write-Host "  wrote:         $path"
}

# --- A. Skip bounce loop entirely --------------------------------------------
# Planted bug: bounces never run. Any [CLARIFY]/[CONTESTED] markers from the
# compose phase will remain until verify, which should produce a non-zero
# marker count and (hopefully) catch the issue as Convergence=PARTIAL/FAIL.
$regA = $source -replace `
    '(?m)^\s*for \(\$passNumber = 1; \$passNumber -le \$maxBounces; \$passNumber\+\+\) \{', `
    'for ($passNumber = 1; $passNumber -le 0; $passNumber++) {  # REGRESSION: bounce disabled'

if ($regA -eq $source) { throw "regression-a patch did not apply -- the target for-loop text changed" }
Write-Regression -Name "regression-a-skip-bounce.ps1" -Content $regA -Description "bounce loop disabled (never iterates)"

# --- B. Truncate compose output --------------------------------------------
# Planted bug: after compose, the plan is sliced to 50 chars before any further
# phases see it. Downstream: plan_quality FAIL (way under min_word_count),
# execution_fidelity probably FAIL (no Files to Change section).
$regB = $source -replace `
    '(?m)(\$composeResult = Invoke-AgentExecPrompt -Provider \$Composer -PhaseName "compose" -PromptText \$composePrompt -WorkingDirectory \$script:RepoRoot\s*\r?\n\$currentPlan = Normalize-AgentDocument -Content \$composeResult\.content\s*\r?\n)', `
    "`$1`$currentPlan = `$currentPlan.Substring(0, [Math]::Min(50, `$currentPlan.Length))  # REGRESSION: truncate plan`r`n"

if ($regB -eq $source) { throw "regression-b patch did not apply -- compose assignment text changed" }
Write-Regression -Name "regression-b-truncate-plan.ps1" -Content $regB -Description "compose output truncated to 50 chars"

# --- C. Skip writing verdict.json -------------------------------------------
# Planted bug: verify phase runs but never persists verdict.json. Scorer's
# verify_accuracy dimension must flag this (no verdict -> FAIL).
$regC = $source -replace `
    '(?m)^\s*Write-RunText -RelativePath "verdict\.json" -Content \$verdictText \| Out-Null', `
    '# REGRESSION: verdict.json write suppressed  (Write-RunText -RelativePath "verdict.json" -Content $verdictText | Out-Null)'

if ($regC -eq $source) { throw "regression-c patch did not apply -- verdict.json write line changed" }
Write-Regression -Name "regression-c-no-verdict.ps1" -Content $regC -Description "verdict.json never written"

# --- D. Hardcode APPROVED verdict -------------------------------------------
# Planted bug: regardless of what Claude/Codex return, verdict.json is written
# as APPROVED with an empty issues list. Should be detected ONLY on cases that
# have `expectations.verify_accuracy.must_catch_issue: true` (case 04). That's
# the test: eval's verify_accuracy dimension must distinguish rubber-stamping
# from a genuine APPROVED.
$hardcoded = @'
$verdictText = '{ "verdict": "APPROVED", "confidence": 90, "summary": "Regression D: hardcoded approve.", "issues": [], "scope_creep_detected": false, "iteration_notes": "" }'
'@
$regD = $source -replace `
    '(?m)^\s*\$verdictText = \$verifyResult\.content\.Trim\(\)\s*\r?\n(\s*\$verdict = \$verdictText \| ConvertFrom-Json)', `
    ($hardcoded + "`r`n`$1")

if ($regD -eq $source) { throw "regression-d patch did not apply -- verify verdict parsing line changed" }
Write-Regression -Name "regression-d-fake-approve.ps1" -Content $regD -Description "verdict.json hardcoded to APPROVED"

Write-Host ""
Write-Host "All regression runners generated in: $outDir"
